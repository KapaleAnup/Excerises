package JavaTutorials.Javaloops;

public class BreakStatement {

    //while break statement if the value gets matched it will break and will not go further.
    public static void main(String[] args) {

        //break
        for(int i= 1; i<=10;i++){
            if(i==5){
                break;
            }
            System.out.println(i);
        }

    }
}
