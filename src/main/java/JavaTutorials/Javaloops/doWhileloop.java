package JavaTutorials.Javaloops;

public class doWhileloop {

    public static void main(String[] args) {
        int i =1;

        do {
            System.out.println(i);
            i++;
        }
        while (i<=10);



        //print odd number

        int j = 1;

        do{
            System.out.println(j);
            j+=2;

        }while (j<=10);
    }
}
